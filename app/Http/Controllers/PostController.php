    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        // variable aan maken en alle blog posts hierin opslaan vanaf de database
        $posts =Post::all();

        //return de view en voeg het toe aan bovenstaande variable
        return view('posts.index')->withPosts($posts);
    }

    /**
